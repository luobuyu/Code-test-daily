class Solution
{
public:
    const static int maxn = 1e5 + 10;
    const static int maxm = 1e5 + 10;
    const int INF = 0x3f3f3f3f;

    bool canPartitionKSubsets(vector<int> &nums, int k)
    {
        int n = nums.size();
        int sum = 0;
        int maxx = 0;
        for (auto &x : nums)
        {
            sum += x;
            maxx = max(maxx, x);
        }
        if (sum % k != 0 || maxx > sum / k)
            return false;
        unordered_map<int, bool> mp;
        int vis = 0;
        function<bool(int, int, int, int, int)>
            dfs = [&](int step, int value, int k, int target, int state)
        {
            if (k == 0)
                return true;
            if (value == 0)
            {
                mp[state] = dfs(0, target, k - 1, target, state);
                return mp[state];
            }
            if (value < 0)
                return false;
            if (step == n)
                return false;
            if (mp.count(state))
                return mp[state];
            for (int i = step; i < n; ++i)
            {
                if (((state >> i) & 1) || value - nums[i] < 0)
                    continue;
                state |= 1 << i;
                if (dfs(i + 1, value - nums[i], k, target, state))
                {
                    return true;
                }
                state ^= 1 << i;
            }
            return false;
        };
        return dfs(0, sum / k, k, sum / k, vis);
    }
};

