class Solution
{
public:
    const static int maxn = 1e5 + 10;
    const static int maxm = 1e5 + 10;
    const int INF = 0x3f3f3f3f;
    int maximizeTheProfit(int n, vector<vector<int>> &offers)
    {
        sort(offers.begin(), offers.end(), [&](const vector<int> &x, const vector<int> &y)
             { return x[1] < y[1]; });
        vector<int> dp(n, 0);
        dp[0] = offers[0][2];
        int m = offers.size();
        for (int i = 1; i < m; ++i)
        {
            int l = 0, r = i - 1;
            int ans = -1;
            dp[i] = max(offers[i][2], dp[i - 1]);
            while (l <= r)
            {
                int mid = (l + r) >> 1;
                if (offers[mid][1] < offers[i][0])
                {
                    ans = mid;
                    l = mid + 1;
                }
                else
                {
                    r = mid - 1;
                }
            }
            if (ans != -1)
            {
                dp[i] = max(dp[i], dp[ans] + offers[i][2]);
            }
        }
        return dp[m - 1];
    }
};

