class Solution
{
public:
    ListNode *doubleIt(ListNode *head)
    {
        string s;
        ListNode *cur = head;
        while (cur)
        {
            s.push_back(cur->val + '0');
            cur = cur->next;
        }
        reverse(s.begin(), s.end());
        int add = 0;
        int i = 0;
        int n = s.length();
        while (i < n || add)
        {
            int tmp;
            if (i < n)
                tmp = s[i] - '0' + s[i] - '0' + add;
            else
                tmp = add;
            add = tmp / 10;
            tmp = tmp % 10;
            if (i < n)
                s[i] = tmp + '0';
            else
                s.push_back(tmp + '0');
            ++i;
        }
        reverse(s.begin(), s.end());
        cur = head;
        i = 0;
        n = s.length();
        while (i < n)
        {
            cur->val = s[i] - '0';
            if (cur->next == nullptr)
            {
                if (i < n - 1)
                    cur->next = new ListNode();
            }
            cur = cur->next;
            i++;
        }
        return head;
    }
};
