class Solution
{
public:
    const static int maxn = 1e5 + 10;
    const static int maxm = 1e5 + 10;
    const static long long mod = 1e9 + 7;
    const long long INF_LL = 0x3f3f3f3f3f3f3f3f;
    const int INF = 0x3f3f3f3f;
    int maxmiumScore(vector<int> &cards, int cnt)
    {
        int sum = 0;
        int n = cards.size();
        sort(cards.begin(), cards.end(), greater<int>());
        int min_even = INF; // 最小的偶数
        int min_odd = INF; // 最小的奇数
        for (int i = 0; i < cnt; ++i) {
            if(cards[i] & 1)
                min_odd = min(min_odd, cards[i]);
            else
                min_even = min(min_even, cards[i]);
            sum += cards[i];
        }
        int max_even = 0, max_odd = 0;
        for (int i = cnt; i < n; ++i) {
            if (cards[i] & 1)
                max_odd = max(max_odd, cards[i]);
            else
                max_even = max(max_even, cards[i]);
        }
        if(sum & 1) {
            // 删去最小的奇数，加入最大的偶数
            if(min_odd != INF && max_even != 0) {
                sum = max(sum, sum - min_odd + max_even);
            } else if(min_even != INF && max_odd != 0) // 删除最小的偶数，加入最大的奇数
            {
                sum = max(sum, sum - min_even + max_odd);
            } else {
                return 0;
            }
        }
        return sum;
    }
};

