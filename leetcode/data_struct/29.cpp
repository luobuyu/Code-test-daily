    int sgn(int x)
    {
        if (x == 0)
            return 0;
        return x > 0 ? 1 : -1;
    }
    int divide(int dividend, int divisor)
    {
        int flag = sgn(dividend) * sgn(divisor);
        dividend = -abs(dividend);
        divisor = -abs(divisor);
        vector<int> times = {0};
        int tmp = divisor;
        int half = dividend >> 1;
        while (tmp >= dividend)
        {
            times.emplace_back(tmp);
            if (tmp < half)
                break;
            tmp = tmp + tmp;
        }
        int ans = 0;
        while (dividend <= divisor)
        {
            int index = upper_bound(times.begin(), times.end(), dividend, [](int x, int y)
                                    { return x > y; }) -
                        times.begin() - 1;
            ans += 1 << (index - 1);
            dividend -= times[index];
        }
        if (ans == INT_MIN)
            if (flag == 1)
                return INT_MAX;
            else
                return INT_MIN;
        else
            return flag * abs(ans);
    }
