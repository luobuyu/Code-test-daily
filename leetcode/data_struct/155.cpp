    {
        s.push(val);
        if (st.empty() || st.top() >= val)
            st.push(val);
    }

    void pop()
    {
        int tmp = s.top();
        s.pop();
        if (st.size() && tmp == st.top())
            st.pop();
    }

    int top()
    {
        return s.top();
    }

    int getMin()
    {
        return st.top();
    }
